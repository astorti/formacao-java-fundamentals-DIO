package BasicoJava.Fundamentos;

public class TiposPrimitivos {

    public static void main(String[] args) {
        byte numberOne = 1;
        short numberTwo = 2;
        int numberThree = 3;
        long numberFour = 4L;
        float numberFive = 5.0f;
        double numberSix = 6.0d;
        String nome = "Java";
        char character = 'a';
        boolean verdadeiro = true;

        System.out.printf("%s, %s, %s, %s, %s, %s, %s, %s, %s \n", numberOne, numberTwo, numberThree, numberFour, numberFive, numberSix, nome, character, verdadeiro);
    }
}
