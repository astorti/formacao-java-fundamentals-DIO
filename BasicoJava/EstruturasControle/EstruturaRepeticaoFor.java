package BasicoJava.EstruturasControle;

public class EstruturaRepeticaoFor {

    public static void main(String[] args) {

        for (int i = 1; i <= 100; i++){
            if (i == 100) {
                System.out.println(i);
            } else {
                System.out.print(i + ", ");
            }
        }
    }
}
